package internal

import (
	"server/db/remote"
	"github.com/name5566/leaf/gate"
)

func init() {
	skeleton.RegisterChanRPC("NewAgent", rpcNewAgent)
	skeleton.RegisterChanRPC("CloseAgent", rpcCloseAgent)
	skeleton.RegisterChanRPC("G2D_PlayerIncrId", remote.G2D_PlayerIncrId)
	skeleton.RegisterChanRPC("G2D_CreatePlayer", remote.G2D_CreatePlayer)
}

func rpcNewAgent(args []interface{}) {
	a := args[0].(gate.Agent)
	_ = a
}

func rpcCloseAgent(args []interface{}) {
	a := args[0].(gate.Agent)
	_ = a
}
